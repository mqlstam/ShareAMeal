package com.example.shareameal;

public class Meal {
    private int id;
    private String name;
    private String description;
    private String imageUrl;
    private String price;


    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getDescription() {
        return description;
    }

    public String getImageUrl() {
        return imageUrl;
    }

    public String getPrice() {
        return price;
    }
}

